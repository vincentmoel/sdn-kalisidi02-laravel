    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        $showUsers = User::where('role_id','=','2')
            ->latest()
            ->paginate(10);

        return view('admin.Users.index',[
            'users' => $showUsers,
        ]);
    }

